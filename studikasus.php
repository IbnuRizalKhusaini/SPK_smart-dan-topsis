        <h2>Kombinasi Metode</h2>
        <p>1. Simple Multi Attribute Rating Technique (SMART)</p>
        <p>Metode Simple Multi Attribute Rating Technique (SMART) merupakan metode yang dapat menyelesaikan masalah multi kriteria dalam pengambilan keputusan. Metode ini pertama kali dikemukakan oleh Edward pada tahun 1997 . Teknik pengambilan keputusan multi kriteria ini didasarkan pada teori bahwa setiap alternatif terdiri dari nilai-nilai pada sejumlah kriteria dan setiap kriteria memiliki nilai bobot yang mempunyai tingkat kepentingan lebih tinggi dibandingankan dengan kriteria lain.</p>
        <p>2. Technique for Others Preference by Similarity (TOPSIS)</p>
        <p>Metode TOPSIS pertama kali dikembangkan oleh Hwang dan Yoon pada tahun 1981 Metode TOPSIS merupakan sebuah metode dengan beberapa kriteria yang digunakan dalam mengidentifikasi solusi dari satu set alternatif yang terbatas. Dalam metode TOPSIS, alternatif yang optimal adalah yang paling jauh dari solusi ideal negatif dan yang paling dekat dengan solusi ideal positif. Solusi ideal positif dapat diartikan sebagai jumlah dari seluruh nilai terbaik pada setiap atribut, sedangkan solusi ideal negatif terdiri dari seluruh nilai terburuk pada setiap atribut.</p>
